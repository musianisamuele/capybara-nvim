-- Settings for vim

-- Numbers and relative numbers for lines
vim.opt.nu = true
vim.opt.relativenumber = true

-- Tabs
vim.opt.tabstop = 4
vim.opt.softtabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true

vim.opt.smartindent = true

-- Make the top and bottom borders to be 8 lines
vim.opt.scrolloff = 8

vim.opt.updatetime = 50

-- 80 char limit
vim.opt.colorcolumn = "80"

vim.opt.mouse = ""
